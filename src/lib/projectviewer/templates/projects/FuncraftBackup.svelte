<h1>Maps</h1>
Most of the maps from the Minecraft server itself were downloaded, right before it closing down.
AFAIK the only things that could be missing/incomplete are:
<ul>
    <li>Map names for infected/shootcraft</li>
    <li>Some infected maps completely missing (unsure)</li>
    <li>3 infected maps which aren't saved completely up to their borders (cs_sable, mars, prison)</li>
    <li>Some skywars maps (can't know as they aren't provided by Funcraft, but pretty sure there are some missing)</li>
</ul>
